<p>Aqui você verá o conteúdo do email que normalmente é enviado no WordPress.</p>
<p>O modelo de e-mail é responsivo e totalmente personalizável. Espero que você goste!</p>

<h3>Placeholders</h3>
<p>Você pode usar qualquer um dessas "mascaras" reservadas no conteúdo ou nos modelos de seus e-mails e eles serão substituídos automaticamente</p>
<ul>
	<li>%%BLOG_URL%%</li>
	<li>%%HOME_URL%%</li>
	<li>%%BLOG_NAME%%</li>
	<li>%%BLOG_DESCRIPTION%%</li>
	<li>%%ADMIN_EMAIL%%</li>
	<li>%%DATE%%</li>
	<li>%%TIME%%</li>
	<li>%%USER_EMAIL%%</li>
</ul>
